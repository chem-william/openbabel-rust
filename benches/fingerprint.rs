fn get_ecfp(smiles: &String, kind: &openbabel::fingerprint::Kind) -> cxx::UniquePtr<cxx::CxxVector<u32>> {
    openbabel::fingerprint::get_fingerprint(smiles, kind, THREAD_ID)
}

fn get_ecfp_for_mols(smiles_vec: &Vec<String>, kind: &openbabel::fingerprint::Kind) {
    for smiles in smiles_vec.iter() {
        openbabel::fingerprint::get_fingerprint(smiles, kind, THREAD_ID);
    }
}

